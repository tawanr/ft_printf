#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*out;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	out = ft_itoa(n);
	while (*out)
	{
		write(fd, out, 1);
		out++;
	}
}
